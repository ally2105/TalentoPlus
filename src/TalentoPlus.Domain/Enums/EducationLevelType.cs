namespace TalentoPlus.Domain.Enums;

/// <summary>
/// Representa los niveles educativos alcanzados por un empleado
/// </summary>
public enum EducationLevelType
{
    /// <summary>
    /// Ninguna educación formal
    /// </summary>
    Ninguno = 0,

    /// <summary>
    /// Educación primaria
    /// </summary>
    Primaria = 1,

    /// <summary>
    /// Educación secundaria/bachillerato
    /// </summary>
    Secundaria = 2,

    /// <summary>
    /// Técnico o tecnólogo
    /// </summary>
    Tecnico = 3,

    /// <summary>
    /// Pregrado universitario
    /// </summary>
    Pregrado = 4,

    /// <summary>
    /// Especialización
    /// </summary>
    Especializacion = 5,

    /// <summary>
    /// Maestría
    /// </summary>
    Maestria = 6,

    /// <summary>
    /// Doctorado
    /// </summary>
    Doctorado = 7
}
